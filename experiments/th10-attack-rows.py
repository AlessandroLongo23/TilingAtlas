import json, re, math, cmath, csv
from collections import defaultdict
from math import gcd

GJ='/sessions/relaxed-peaceful-davinci/mnt/TilingAtlas/figures/data/galebach.json'
WT='/sessions/relaxed-peaceful-davinci/mnt/Thesis/thesis/figures/charts/weight-tightness.csv'

B=[cmath.exp(1j*math.pi/6*j) for j in range(4)]
def cx(v): return sum(v[j]*B[j] for j in range(4))
def dot(a,b): return (a.conjugate()*b).real
def cross(a,b): return (a.conjugate()*b).imag   # z-comp of a x b
def reduce2(v1,v2):
    for _ in range(1000):
        if dot(v2,v2)<dot(v1,v1): v1,v2=v2,v1
        d1=dot(v1,v1)
        if d1==0: break
        m=round(dot(v1,v2)/d1)
        if m==0: break
        v2=v2-m*v1
    return v1,v2
def classify(v1,v2):
    l1=abs(v1); l2=abs(v2)
    if abs(l1-l2)>1e-5*max(l1,l2): return 'rect/obl(E)'
    ang=math.degrees(math.acos(max(-1,min(1,dot(v1,v2)/(l1*l2)))))
    if abs(ang-90)<1e-3: return 'square(R)'
    if abs(ang-60)<1e-3 or abs(ang-120)<1e-3: return 'hex(R)'
    return 'rhombic(E)'

txt=open(GJ).read(); txt=txt[txt.index('{'):]; txt=re.sub(r',(\s*[}\]])', r'\1', txt); G=json.loads(txt)
kof={}
for r in csv.DictReader(open(WT)): kof[r['id']]=int(r['k'])

def count_distinct_mod(vals, P, tol=1e-6):
    xs=sorted(v % P for v in vals)
    reps=[]
    for x in xs:
        placed=False
        for rr in reps:
            d=abs(x-rr); d=min(d,P-d)
            if d<=tol: placed=True; break
        if not placed: reps.append(x)
    return len(reps)

def period_for(w,v1,v2,A):
    # height subgroup along u=perp(w) generated by cross(w,v1),cross(w,v2), both = integer*A
    g1=cross(w,v1); g2=cross(w,v2)
    r1=round(g1/A); r2=round(g2/A)
    gg=gcd(abs(r1),abs(r2))
    return gg*A if gg>0 else A

elong=[]; degenerate=[]; round_cnt=0
for tid,e in G.items():
    T1=cx(e['T1']); T2=cx(e['T2'])
    if abs(T1)<1e-9 or abs(T2)<1e-9: degenerate.append(tid); continue
    v1,v2=reduce2(T1,T2)
    lat=classify(v1,v2)
    k=kof.get(tid,int(tid[1]))
    if '(R)' in lat: round_cnt+=1; continue
    seed=[cx(s) for s in e['Seed']]
    A=abs(cross(v1,v2))
    cands={'perp_v1':v1,'perp_v2':v2,'perp_v1+v2':v1+v2,'perp_v1-v2':v1-v2}
    rows={}
    for name,w in cands.items():
        if abs(w)<1e-9: continue
        P=period_for(w,v1,v2,A)
        rows[name]=count_distinct_mod([cross(w,s) for s in seed], P)
    best=min(rows.values())
    worst=max(rows.values())
    elong.append((tid,k,lat,rows,best,worst,2*k+2,2*k,len(seed)))

print(f"total tilings parsed: {len(G)}  degenerate(zeroT): {len(degenerate)} {degenerate}")
print(f"round (excluded): {round_cnt}   elongated: {len(elong)}")

