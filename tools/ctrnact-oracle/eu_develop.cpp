// eu_develop — native C++ port of develop.py: pruned Čtrnáct combinatorial solutions → EXACT period
// cells {id,k,T1,T2,Seed} in ℤ[ζ₁₂] (ζ=ζ₁₂, min poly ζ⁴=ζ²−1), Galebach oracle format. Reuses the
// decode machinery already ported in eu_pruner.cpp (vertex-figure tables in pruner_tables.inc), adds
// the geometric developer (flood-fill in exact ℤ[ζ₁₂], integer lattice HNF, Lagrange-Gauss reduction,
// seeds mod Λ). Validated byte-for-byte against develop.py on the same pruned input (k≤13).
//
// Float is used ONLY for lattice reduction / coincidence tests, never to snap positions (ℤ[ζ₁₂] is
// dense in ℂ). To stay bit-identical to develop.py: banker's rounding (nearbyint, Python round()) for
// the reduction coefficient, floor division in egcd, lexicographic seed sort.
//
//   ./eu_develop --kmin 14 --kmax 16 --pruned <dir> --out cells.json   [--sign 1]
