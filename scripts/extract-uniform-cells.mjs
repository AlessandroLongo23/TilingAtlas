// Regenerates lib/render/uniformCells.ts — the 11 uniform tilings' translational cells, lifted out
// of public/reference-atlas.json as a source constant.
//
// Why a copy: the error and 404 screens draw these, and an error screen has no server data to reach
// (app/error.tsx is a client boundary that renders after something already failed). 7 kB of vertices
// inlined in the bundle beats a fetch that can fail on the one page whose job is to survive failure.
// tests/uniform-cells.test.ts asserts the constant still matches the atlas, so it cannot drift.
//
// Usage: node scripts/extract-uniform-cells.mjs

import { readFile, writeFile } from "node:fs/promises";
import path from "node:path";

const root = process.cwd();
const atlas = JSON.parse(await readFile(path.join(root, "public/reference-atlas.json"), "utf8"));

// Same selection as lib/services/landingData.ts pickUniformEleven.
const eleven = atlas
	.filter((t) => t.source === "galebach" && t.k === 1 && !t.family.includes("*"))
	.sort((a, b) => a.id.localeCompare(b.id));

if (eleven.length !== 11) {
	throw new Error(`expected 11 uniform tilings, found ${eleven.length}`);
}

// `+ 0` folds -0 (which JSON.stringify writes as 0) so the file round-trips through the test.
const round = (v) => Number(v.toFixed(6)) + 0;
const roundCell = (cell) => ({
	p: (cell.p ?? cell.cellPolygons ?? []).map((poly) => ({
		n: poly.n ?? (poly.v ?? poly.vertices ?? []).length,
		v: (poly.v ?? poly.vertices ?? []).map((pt) =>
			Array.isArray(pt) ? [round(pt[0]), round(pt[1])] : [round(pt.x), round(pt.y)],
		),
	})),
	b: (cell.b ?? cell.basis).map((row) => row.map(round)),
});

const entries = eleven.map((t) => ({ id: t.id, family: t.family, cell: roundCell(t.renderCell) }));

const body = entries
	.map(
		(e) =>
			`\t{\n\t\tid: ${JSON.stringify(e.id)},\n\t\tfamily: ${JSON.stringify(e.family)},\n\t\tcell: ${JSON.stringify(e.cell)},\n\t},`,
	)
	.join("\n");

const out = `// GENERATED by scripts/extract-uniform-cells.mjs — do not edit by hand.
// The 11 uniform tilings (Galebach k = 1 regular shelf, t1001–t1011) as translational cells, so the
// error screens can draw real atlas specimens without server data. Regenerate after an atlas rebake;
// tests/uniform-cells.test.ts fails if this drifts from public/reference-atlas.json.

import type { TranslationalCellData } from "@/lib/utils/renderTiling";

export interface UniformCell {
	id: string;
	/** Raw Galebach family string (compactVertexConfig turns it into the display label). */
	family: string;
	cell: TranslationalCellData;
}

export const UNIFORM_CELLS: UniformCell[] = [
${body}
];
`;

await writeFile(path.join(root, "lib/render/uniformCells.ts"), out, "utf8");
console.log(`wrote lib/render/uniformCells.ts — ${entries.length} cells, ${out.length} bytes`);
