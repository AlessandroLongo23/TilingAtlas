// "Freedraw" tilings (Marek Čtrnáct, 2026-07): the combinatorics lives entirely in which EDGES of an
// underlying grid are drawn, never in the tiles. A pattern is a periodic subset of the edges of Z^2
// with no vertex of degree exactly 1 (a degree-1 vertex is a dead end, which would make the figure a
// maze rather than a tiling). The tiles are whatever faces fall out, and they may be finite polyominoes,
// infinite strips, or 2-dimensionally unbounded regions — so they are NOT tiles in the Grünbaum &
// Shephard sense (closed topological disks) and none of the classical uniform-tiling theory applies.
//
// k = the number of GRID-POINT orbits under the pattern's own symmetry group. Note this counts grid
// points, including those with no drawn edge at all, so k measures how complicated the decoration of
// the grid is, not how complicated the resulting tiling is. Strips of width w give k ≈ w.
//
// The period lattice is stored in Hermite normal form: generated by (a, 0) and (b, d), index n = a*d,
// with 0 <= b < a. Bits are indexed BY GRID VERTEX, one vertex per lattice coset:
//   h[c] — the unit edge from vertex c east to c + (1,0)
//   v[c] — the unit edge from vertex c north to c + (0,1)
// Cells (the unit squares) share the same index scheme via their lower-left vertex.

export interface FreedrawPattern {
	/** Stable catalogue id, e.g. "fd-2-017". */
	id: string;
	/** Number of grid-point orbits under the pattern's symmetry group. */
	k: number;
	/** Period lattice in HNF: generated by (a, 0) and (b, d). */
	a: number;
	b: number;
	d: number;
	/** East edge drawn, per lattice coset. Length a*d. */
	h: number[];
	/** North edge drawn, per lattice coset. Length a*d. */
	v: number[];
	/** Grid-point orbit label in [0, k), per lattice coset. Length a*d. */
	orbit: number[];
}

export const cosetCount = (p: Pick<FreedrawPattern, "a" | "d">) => p.a * p.d;

/**
 * Index of the lattice coset containing the integer point (x, y). Reduces y modulo d using the
 * (b, d) generator, then x modulo a using the (a, 0) generator. Works for negative coordinates:
 * Math.floor gives the correct rounding direction, unlike a bare `%`.
 */
export function coset(p: FreedrawPattern, x: number, y: number): number {
	const q = Math.floor(y / p.d);
	const j = y - q * p.d;
	const i = ((x - q * p.b) % p.a + p.a) % p.a;
	return j * p.a + i;
}

/** Is the unit edge from (x, y) east to (x+1, y) drawn? */
export const drawnEast = (p: FreedrawPattern, x: number, y: number) => p.h[coset(p, x, y)] === 1;

/** Is the unit edge from (x, y) north to (x, y+1) drawn? */
export const drawnNorth = (p: FreedrawPattern, x: number, y: number) => p.v[coset(p, x, y)] === 1;

/**
 * Number of drawn edges at grid point (x, y) — 0, 2, 3 or 4 by construction. This is the vertex's
 * type; the four bits (E, N, W, S) are exactly the Carcassonne-tile type Marek draws by hand.
 */
export function degree(p: FreedrawPattern, x: number, y: number): number {
	let n = 0;
	if (drawnEast(p, x, y)) n++;
	if (drawnNorth(p, x, y)) n++;
	if (drawnEast(p, x - 1, y)) n++;
	if (drawnNorth(p, x, y - 1)) n++;
	return n;
}

/**
 * The four drawn directions at (x, y) as a bitmask: 1 = east, 2 = north, 4 = west, 8 = south. The 12
 * legal masks (all 16 minus the four single-bit ones) are the tile alphabet of the Carcassonne view.
 */
export function vertexMask(p: FreedrawPattern, x: number, y: number): number {
	return (
		(drawnEast(p, x, y) ? 1 : 0) |
		(drawnNorth(p, x, y) ? 2 : 0) |
		(drawnEast(p, x - 1, y) ? 4 : 0) |
		(drawnNorth(p, x, y - 1) ? 8 : 0)
	);
}
