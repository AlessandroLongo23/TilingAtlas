// The Reference (Oracle) shelf — a DISPLAY-ONLY atlas of known k-uniform tilings from the literature,
// deliberately kept OFF the certified-results catalogue (§0: the site never produces the claim; these
// are external oracles, not our search output). Generated by scripts/build-reference-atlas.ts into the
// static asset public/reference-atlas.json and lazy-fetched only when the shelf is opened.
//
//   - galebach: regular k=1..6 (Soto-Sánchez/Galebach), reconstructed geometry. No completeness proof.
//   - myers:    Myers star tilings (k=1 in-ring Fig-4 subclass today; k=2 pending the machine-readable
//               JSON). An unchecked hand enumeration — shown as reference, never as certified.
//   - ctrnact:  Marek Čtrnáct's k-uniform search, reproduced here (k=7 shown; k≤8 in figures/data/ctrnact.json).
//               An unproven exhaustive search extending Galebach beyond k=6 — reference, never certified.
//   - ctrnact-star: the Čtrnáct-engine star extension's full in-ring catalogs (feat/ctrnact-star),
//               k=1..2, star-bearing solutions only. Most reproduce Myers records; the ones flagged
//               candidate:true match NOTHING in Myers and are pending adversarial review.
//   - composable: tilings using COMPOSITE convex super-tiles (e.g. cx4-2.4.2.4) alongside the regular
//               set (scripts/build-composable-atlas.ts → the separate public/reference-atlas-composable
//               .json). Distinct-tiling counts are EXACT (proof-grade ℤ[ζ₂₄] congruence dedup, NOTES §55);
//               enumeration completeness rests on the engine, as for the regular family. These carry no
//               certification / discoverer-completeness / wallpaper classification.
export type Certification = "proven" | "reproduced" | "candidate";

export interface ReferenceTiling {
	id: string; // "t4001" (galebach) | "myers-k1-star-03" (myers) | "ctrnact-07_..." (ctrnact)
	source: "galebach" | "myers" | "ctrnact" | "ctrnact-star" | "composable";
	k: number;
	family: string; // distinct polygon-type label, e.g. "3.4.6.12"; star tiles marked "n*"
	renderCell: TranslationalCellData; // float, parseBaseCell-ready
	alphaRange?: [number, number]; // degrees; present ⇒ one-parameter family with an alpha slider
	candidate?: boolean; // ctrnact-star only: not in Myers' enumeration — candidate new tiling
	preview?: boolean; // ctrnact-star only: from a PARTIAL (still-running) solve — incomplete, uncertified
	// Historical first-discoverer of this tiling (Kepler, Krötenheerdt, Chavey, Galebach, Čtrnáct,
	// Joseph Myers, Alessandro Longo). One attribute, orthogonal to certification.
	discoverer: string;
	// Rigorous completeness status of this tiling's enumeration level:
	//   proven      — this work's method has a completeness certificate (regular k≤3)
	//   reproduced  — count matches a published enumeration, not independently proven here
	//   candidate   — surfaced by this work; completeness / literature-novelty not established
	// Absent on the composable demo shelf — those tilings make no completeness claim, so they're
	// excluded from the certification facet rather than assigned a misleading status.
	certification?: Certification;
	// Composable shelf only: true iff every composite tile it uses dissects into regular pieces (the
	// decomposable palette); false iff it uses a non-decomposable composite. Absent on every other source.
	decomposableOnly?: boolean;
	note?: string; // free-text provenance caveat (composable demo: counts are illustrative, not all-and-only)
	// Present on family entries: the proven parametric cell driving the /play alpha slider
	// (lib/utils/paramCell.ts). renderCell then holds the default-alpha evaluation (thumbnails).
	paramCell?: ParametricCellData;
	exactSource?: ExactCellSource; // exact cyclotomic cell for the symmetry overlay (added 2026-07)

	// Vertex-type classification (build-computed, 2026-07). k counts vertex ORBITS; m counts DISTINCT
	// vertex configurations among them (m ≤ k). `partition` is the multiplicities of those distinct
	// configs, descending, summing to k — e.g. m=3, partition [5,1,1] is the "511" group at k=7.
	// Absent when a source has no per-orbit config data (never guessed; the tiling is then excluded from
	// M/partition filters). m === k is the Krötenheerdt (maximal) class.
	m?: number;
	partition?: number[];
	// Exact wallpaper symmetry — REGULAR tilings only (WallpaperSymmetry needs exact convex-tile
	// intersection; star tiles are non-convex ⇒ omitted, NOTES §9.4). Absent for every star tiling.
	wallpaperGroup?: WallpaperGroup;
	latticeShape?: LatticeShape;
}

// The star folds an atlas actually uses (the n of each n* token): 3,4,6,8,9,12,18. Used to seed the
// star-fold filter without re-deriving from the whole atlas.
export const STAR_FOLDS = [3, 4, 6, 8, 9, 12, 18] as const;

// tileClass, the primary shelf axis: "composable" iff the tiling comes from the composite-tile demo
// (source-driven, since a composite family has no "*" token); else "star" iff its family carries a
// star token ("n*"); "regular" otherwise. Matches polygonClassLabel.
export type TileClass = "regular" | "star" | "composable";
export function tileClassOf(t: Pick<ReferenceTiling, "family" | "source">): TileClass {
	if (t.source === "composable") return "composable";
	return t.family.includes("*") ? "star" : "regular";
}

// The star folds present in a family (unique, ascending). "3.4*.6*" → [4,6]. Empty for regular.
export function starFoldsOf(t: Pick<ReferenceTiling, "family">): number[] {
	const folds = new Set<number>();
	for (const tok of t.family.split(".")) {
		if (tok.endsWith("*")) {
			const n = parseInt(tok, 10);
			if (Number.isFinite(n)) folds.add(n);
		}
	}
	return [...folds].sort((a, b) => a - b);
}

// A one-parameter (α-slider) family iff it carries an alphaRange. The rigid star tilings have none.
export function isParametric(t: Pick<ReferenceTiling, "alphaRange">): boolean {
	return Array.isArray(t.alphaRange);
}

// Canonical partition key for display + filtering: digits joined ("511"), switching to a "."
// separator if any part reaches 10 so "10,1" never collides with "1,0,1". Null when unclassified.
export function partitionKeyOf(t: Pick<ReferenceTiling, "partition">): string | null {
	if (!t.partition?.length) return null;
	const sep = t.partition.some((p) => p >= 10) ? "." : "";
	return t.partition.join(sep);
}

// Krötenheerdt (maximal): every orbit has a distinct vertex config, i.e. m === k. False when unclassified.
export function isMaximal(t: Pick<ReferenceTiling, "m" | "k">): boolean {
	return t.m != null && t.m === t.k;
}

export interface ReferenceFilter {
	kValue?: number; // single vertex-orbit count; unset = every k
	tileClass?: TileClass; // regular polygons only / star-bearing only / composite-tile demo
	// Composable shelf facet: keep only decomposable-family or only uses-non-decomposable tilings.
	// Any non-composable tiling (decomposableOnly undefined) is EXCLUDED while this is active.
	composableDecomp?: "decomposable" | "non-decomposable";
	mValue?: number; // single distinct-vertex-config count; unclassified tilings never match
	partitionKey?: string; // single partition key (e.g. "511"); unclassified tilings never match
	maximalOnly?: boolean; // Krötenheerdt: keep only m === k
	starFolds?: number[]; // keep tilings using at least one of these star folds
	parametric?: "rigid" | "family"; // rigid (no α) / one-parameter α-family
	wallpaperGroups?: WallpaperGroup[]; // exact group; unclassified (all stars) never match
	latticeShapes?: LatticeShape[]; // exact lattice shape; unclassified (all stars) never match
	discoverers?: string[]; // each entry's discoverer must be in this set
	certifications?: Certification[]; // proven / reproduced / candidate
	polygonNames?: string[]; // each must appear in the tiling's family label
	query?: string; // free-text substring match on id or family (e.g. "4j5_5b2" or "3.6")
}

export function matchesReferenceFilters(t: ReferenceTiling, f: ReferenceFilter): boolean {
	if (f.kValue != null && t.k !== f.kValue) return false;
	if (f.tileClass && tileClassOf(t) !== f.tileClass) return false;
	if (f.composableDecomp) {
		if (t.decomposableOnly == null) return false; // non-composable tilings never match this facet
		if (f.composableDecomp === "decomposable" && !t.decomposableOnly) return false;
		if (f.composableDecomp === "non-decomposable" && t.decomposableOnly) return false;
	}
	// M/partition/maximal: an active filter EXCLUDES unclassified tilings rather than matching them —
	// completeness ethos, we never silently pass a tiling whose classification we don't have.
	if (f.mValue != null && t.m !== f.mValue) return false;
	if (f.partitionKey != null && partitionKeyOf(t) !== f.partitionKey) return false;
	if (f.maximalOnly && !isMaximal(t)) return false;
	if (f.starFolds?.length) {
		const folds = starFoldsOf(t);
		if (!f.starFolds.some((n) => folds.includes(n))) return false;
	}
	if (f.parametric) {
		const isFam = isParametric(t);
		if (f.parametric === "family" && !isFam) return false;
		if (f.parametric === "rigid" && isFam) return false;
	}
	if (f.wallpaperGroups?.length && (t.wallpaperGroup == null || !f.wallpaperGroups.includes(t.wallpaperGroup))) return false;
	if (f.latticeShapes?.length && (t.latticeShape == null || !f.latticeShapes.includes(t.latticeShape))) return false;
	if (f.discoverers?.length && !f.discoverers.includes(t.discoverer)) return false;
	if (f.certifications?.length && (t.certification == null || !f.certifications.includes(t.certification))) return false;
	if (f.polygonNames?.length) {
		const fam = t.family.split(".").map((s) => s.trim());
		if (!f.polygonNames.every((n) => fam.includes(n))) return false;
	}
	if (f.query?.trim()) {
		const q = f.query.trim().toLowerCase();
		if (!t.id.toLowerCase().includes(q) && !t.family.toLowerCase().includes(q)) return false;
	}
	return true;
}

// Adapt a reference tiling to the CatalogueTiling shape so the /play viewer (Canvas + Sidebar, all
// keyed on canonicalKey) renders it unchanged. `id` stands in for canonicalKey; certified is always
// false (these are oracles, not our proven results) — the /play sidebar is told mode="reference" so it
// shows an "Oracle" pill instead of the misleading "Candidate" badge.
export function referenceToCatalogue(r: ReferenceTiling): CatalogueTiling {
	return {
		canonicalKey: r.id,
		k: r.k,
		family: r.family,
		renderCell: r.renderCell,
		certified: false,
		runIds: [],
		exactSource: r.exactSource,
		paramCell: r.paramCell,
	};
}

// Lazy client-side fetch of the static atlas. Cached across mode toggles so the ~4MB payload is pulled
// at most once per session (the browser also HTTP-caches it). Never bundled into the initial page.
let cache: ReferenceTiling[] | null = null;
let inflight: Promise<ReferenceTiling[]> | null = null;

export async function loadReferenceAtlas(): Promise<ReferenceTiling[]> {
	if (cache) return cache;
	if (inflight) return inflight;
	// The base atlas is required; the composable demo shelf is best-effort — a missing/broken
	// reference-atlas-composable.json degrades to an empty merge, never breaks the library.
	inflight = Promise.all([
		fetch("/reference-atlas.json").then((res) => {
			if (!res.ok) throw new Error(`reference-atlas.json: HTTP ${res.status}`);
			return res.json() as Promise<ReferenceTiling[]>;
		}),
		fetch("/reference-atlas-composable.json")
			.then((res) => (res.ok ? (res.json() as Promise<ReferenceTiling[]>) : []))
			.catch(() => [] as ReferenceTiling[]),
	])
		.then(([base, composable]) => {
			const data = [...base, ...composable];
			cache = data;
			inflight = null;
			return data;
		})
		.catch((err) => {
			inflight = null;
			throw err;
		});
	return inflight;
}

// Per-k lazy shards for the higher-k Čtrnáct atlas (k≥8, generated by scripts/build-reference-atlas.ts
// into public/reference-atlas-k{k}.json). Fetched only when that k is selected in the shelf filter, so
// a heavy shard (k=10 ≈ 73 MB) never loads unless the user opens it. Cached per-k across the session.
// NOTE: these records carry no m/partition/wallpaper classification yet (the build ctrnact path doesn't
// compute it) — they filter by k/tileClass/discoverer/certification and render, but are excluded from
// the M/partition/wallpaper facets until the build stamps those fields.
const shardCache = new Map<number, ReferenceTiling[]>();
const shardInflight = new Map<number, Promise<ReferenceTiling[]>>();

export async function loadReferenceAtlasShard(k: number): Promise<ReferenceTiling[]> {
	const cached = shardCache.get(k);
	if (cached) return cached;
	const existing = shardInflight.get(k);
	if (existing) return existing;
	const p = fetch(`/reference-atlas-k${k}.json`)
		.then((res) => {
			if (!res.ok) throw new Error(`reference-atlas-k${k}.json: HTTP ${res.status}`);
			return res.json() as Promise<ReferenceTiling[]>;
		})
		.then((data) => {
			shardCache.set(k, data);
			shardInflight.delete(k);
			return data;
		})
		.catch((err) => {
			shardInflight.delete(k);
			throw err;
		});
	shardInflight.set(k, p);
	return p;
}

// Per-k lazy shards for the Composable demo shelf (k≥3, generated by scripts/build-composable-atlas.ts
// into public/reference-atlas-composable-k{k}.json). The main reference-atlas-composable.json carries
// only k≤2; the higher-k entries — heaviest today is k=3, ~940 tilings and growing with the convex
// solve — load only when the Composable class or a k≥3 chip is selected. Mirrors loadReferenceAtlasShard,
// but a MISSING shard degrades to an empty merge (HTTP 404 ⇒ []) rather than throwing: the composable
// shelf is a best-effort demo, never a hard dependency of the library. Cached per-k across the session.
const composableShardCache = new Map<number, ReferenceTiling[]>();
const composableShardInflight = new Map<number, Promise<ReferenceTiling[]>>();

export async function loadComposableAtlasShard(k: number): Promise<ReferenceTiling[]> {
	const cached = composableShardCache.get(k);
	if (cached) return cached;
	const existing = composableShardInflight.get(k);
	if (existing) return existing;
	const p = fetch(`/reference-atlas-composable-k${k}.json`)
		.then((res) => {
			if (res.status === 404) return [] as ReferenceTiling[]; // shard not built for this k — empty merge
			if (!res.ok) throw new Error(`reference-atlas-composable-k${k}.json: HTTP ${res.status}`);
			return res.json() as Promise<ReferenceTiling[]>;
		})
		.then((data) => {
			composableShardCache.set(k, data);
			composableShardInflight.delete(k);
			return data;
		})
		.catch((err) => {
			composableShardInflight.delete(k);
			throw err;
		});
	composableShardInflight.set(k, p);
	return p;
}
