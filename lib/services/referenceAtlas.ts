// ── Freedraw ──────────────────────────────────────────────────────────────────────────────────────
// The freedraw shelf's tile-kind facet. A freedraw "tile" is whatever face falls out of the drawn edge
// set, so it can be a finite polyomino (rank 0), an infinite strip (rank 1), or a two-dimensionally
// unbounded sheet (rank 2) — the one axis that says what kind of object a pattern actually produces.
export type FreedrawKind = "finite" | "strip" | "unbounded" | "holes";
export interface FreedrawStats {
	faceOrbits: number;
	finite: number;
	strips: number;
	unbounded: number;
	withHoles: number;
}

// analyseFaces is O(a·d) and every shipped pattern has a·d ≤ 8, but the shelf filter runs over the whole
// atlas on every keystroke — so memoise per pattern id rather than re-flooding 166 patterns per render.
const freedrawStatsCache = new Map<string, FreedrawStats>();

export function freedrawStatsOf(t: Pick<ReferenceTiling, "id" | "freedraw">): FreedrawStats | null {
	if (!t.freedraw) return null;
	const hit = freedrawStatsCache.get(t.id);
	if (hit) return hit;
	const stats = summarise(analyseFaces(t.freedraw));
	freedrawStatsCache.set(t.id, stats);
	return stats;
}

export function matchesFreedrawKind(s: FreedrawStats, kind: FreedrawKind): boolean {
	if (kind === "finite") return s.strips === 0 && s.unbounded === 0;
	if (kind === "strip") return s.strips > 0;
	if (kind === "unbounded") return s.unbounded > 0;
	return s.withHoles > 0;
}

// The card / search label for a freedraw pattern: what its faces ARE, since there is no vertex
// configuration to name it by. "1 strip + 2 polyominoes", "1 unbounded · holes".
export function freedrawFamilyLabel(s: FreedrawStats): string {
	const parts: string[] = [];
	if (s.finite) parts.push(`${s.finite} ${s.finite === 1 ? "polyomino" : "polyominoes"}`);
	if (s.strips) parts.push(`${s.strips} ${s.strips === 1 ? "strip" : "strips"}`);
	if (s.unbounded) parts.push(`${s.unbounded} unbounded`);
	return (parts.join(" + ") || "empty") + (s.withHoles ? " · holes" : "");
}

// The star folds present in a family (unique, ascending). "3.4*.6*" → [4,6]. Empty for regular.
export function starFoldsOf(t: Pick<ReferenceTiling, "family">): number[] {
	const folds = new Set<number>();
	for (const tok of t.family.split(".")) {
		if (tok.endsWith("*")) {
			const n = parseInt(tok, 10);
			if (Number.isFinite(n)) folds.add(n);
		}
	}
	return [...folds].sort((a, b) => a - b);
}

// A one-parameter (α-slider) family iff it carries an alphaRange. The rigid star tilings have none.
export function isParametric(t: Pick<ReferenceTiling, "alphaRange">): boolean {
	return Array.isArray(t.alphaRange);
}

// Isotoxal shelf only: how many INDEPENDENT free angles the family flexes on (its paramCell slider count
// P). 1 ⇒ a single isotoxal tile flexes (α-family); 2 ⇒ two isotoxal tiles flex on their own (α, β-family).
// Null for any non-isotoxal tiling, or an isotoxal record missing its parametric cell — those never match
// the Shape facet, matching the exclude-the-unclassified ethos used by the M/partition filters.
export function isotoxalParamCount(t: Pick<ReferenceTiling, "source" | "paramCell">): 1 | 2 | null {
	if (t.source !== "isotoxal") return null;
	const n = t.paramCell?.params?.length;
	return n === 1 || n === 2 ? n : null;
}

// Canonical partition key for display + filtering: digits joined ("511"), switching to a "."
// separator if any part reaches 10 so "10,1" never collides with "1,0,1". Null when unclassified.
export function partitionKeyOf(t: Pick<ReferenceTiling, "partition">): string | null {
	if (!t.partition?.length) return null;
	const sep = t.partition.some((p) => p >= 10) ? "." : "";
	return t.partition.join(sep);
}

// Krötenheerdt (maximal): every orbit has a distinct vertex config, i.e. m === k. False when unclassified.
export function isMaximal(t: Pick<ReferenceTiling, "m" | "k">): boolean {
	return t.m != null && t.m === t.k;
}

export interface ReferenceFilter {
	// The geometry axis — the library's top-level split, one level above tileClass. When set, only tilings
	// of this geometry match. The Euclidean-only sub-filters (tileClass, star, lattice, wallpaper group)
	// are meaningless off the plane, so the shelf hides them for hyperbolic/spherical.
	geometry?: Geometry;
	kValue?: number; // single vertex-orbit count; unset = every k
	tileClass?: TileClass; // regular polygons only / star-bearing only / composite-tile demo
	// Scaled shelf sub-class: "s12" keeps only tilings within sides {1,2} (the former Doubled class);
	// "s123" keeps only tilings that use a side-3 tile. Non-scaled tilings never match while this is active.
	scaledScaleSet?: "s12" | "s123";
	// Polyomino shelf sub-class: keep only tilings whose tiles are of this polyomino order ("tetromino" today).
	// Non-polyomino tilings never match while this is active.
	polyominoOrder?: "tetromino";
	// Islamic shelf sub-class: keep only tilings in this Bonner design system. Non-Islamic tilings never
	// match while this is active.
	islamicSystem?: IslamicSystem;
	// Freedraw shelf sub-class: keep only patterns whose faces are all finite / include a strip / include an
	// unbounded sheet / include a polyomino with holes. Non-freedraw tilings never match while this is active.
	freedrawKind?: FreedrawKind;
	// Convex-irregular shelf facet: keep only decomposable-family or only uses-non-decomposable tilings.
	// Any tiling outside that class (decomposableOnly undefined) is EXCLUDED while this is active.
	convexDecomp?: "decomposable" | "non-decomposable";
	mValue?: number; // single distinct-vertex-config count; unclassified tilings never match
	partitionKey?: string; // single partition key (e.g. "511"); unclassified tilings never match
	maximalOnly?: boolean; // Krötenheerdt: keep only m === k
	starFolds?: number[]; // keep tilings using at least one of these star folds
	parametric?: "rigid" | "family"; // rigid (no α) / one-parameter α-family
	isotoxalShape?: "alpha" | "alpha-beta"; // isotoxal only: 1 free angle (α) vs 2 independent (α, β)
	wallpaperGroups?: WallpaperGroup[]; // exact group; unclassified (all stars) never match
	latticeShapes?: LatticeShape[]; // exact lattice shape; unclassified (all stars) never match
	discoverers?: string[]; // each entry's discoverer must be in this set
	certifications?: Certification[]; // proven / reproduced / candidate
	polygonNames?: string[]; // each must appear in the tiling's family label
	query?: string; // free-text substring match on id or family (e.g. "4j5_5b2" or "3.6")
}

export function matchesReferenceFilters(t: ReferenceTiling, f: ReferenceFilter): boolean {
	if (f.geometry && geometryOf(t) !== f.geometry) return false;
	if (f.kValue != null && t.k !== f.kValue) return false;
	if (f.tileClass && tileClassOf(t) !== f.tileClass) return false;
	if (f.scaledScaleSet) {
		const ms = scaledMaxScaleOf(t);
		if (ms == null) return false; // non-scaled tilings never match the scale-set facet
		if (f.scaledScaleSet === "s12" && ms > 2) return false;
		if (f.scaledScaleSet === "s123" && ms < 3) return false;
	}
	if (f.polyominoOrder) {
		if (polyominoOrderOf(t) !== f.polyominoOrder) return false; // non-polyomino tilings never match the order facet
	}
	if (f.islamicSystem) {
		if (islamicSystemOf(t) !== f.islamicSystem) return false; // non-Islamic tilings never match the system facet
	}
	if (f.freedrawKind) {
		const s = freedrawStatsOf(t);
		if (!s || !matchesFreedrawKind(s, f.freedrawKind)) return false; // non-freedraw never matches the kind facet
	}
	if (f.convexDecomp) {
		if (t.decomposableOnly == null) return false; // tilings outside the convex-irregular class never match this facet
		if (f.convexDecomp === "decomposable" && !t.decomposableOnly) return false;
		if (f.convexDecomp === "non-decomposable" && t.decomposableOnly) return false;
	}
	// M/partition/maximal: an active filter EXCLUDES unclassified tilings rather than matching them —
	// completeness ethos, we never silently pass a tiling whose classification we don't have.
	if (f.mValue != null && t.m !== f.mValue) return false;
	if (f.partitionKey != null && partitionKeyOf(t) !== f.partitionKey) return false;
	if (f.maximalOnly && !isMaximal(t)) return false;
	if (f.starFolds?.length) {
		const folds = starFoldsOf(t);
		if (!f.starFolds.some((n) => folds.includes(n))) return false;
	}
	if (f.parametric) {
		const isFam = isParametric(t);
		if (f.parametric === "family" && !isFam) return false;
		if (f.parametric === "rigid" && isFam) return false;
	}
	if (f.isotoxalShape) {
		const p = isotoxalParamCount(t);
		if (f.isotoxalShape === "alpha" && p !== 1) return false;
		if (f.isotoxalShape === "alpha-beta" && p !== 2) return false;
	}
	if (f.wallpaperGroups?.length && (t.wallpaperGroup == null || !f.wallpaperGroups.includes(t.wallpaperGroup))) return false;
	if (f.latticeShapes?.length && (t.latticeShape == null || !f.latticeShapes.includes(t.latticeShape))) return false;
	if (f.discoverers?.length && !f.discoverers.includes(t.discoverer)) return false;
	if (f.certifications?.length && (t.certification == null || !f.certifications.includes(t.certification))) return false;
	if (f.polygonNames?.length) {
		const fam = t.family.split(".").map((s) => s.trim());
		if (!f.polygonNames.every((n) => fam.includes(n))) return false;
	}
	if (f.query?.trim()) {
		const q = f.query.trim().toLowerCase();
		if (!t.id.toLowerCase().includes(q) && !t.family.toLowerCase().includes(q)) return false;
	}
	return true;
}

// Adapt a reference tiling to the CatalogueTiling shape so the /play viewer (Canvas + Sidebar, all
// keyed on canonicalKey) renders it unchanged. `id` stands in for canonicalKey; certified is always
// false (these are oracles, not our proven results) — the /play sidebar is told mode="reference" so it
// shows an "Oracle" pill instead of the misleading "Candidate" badge.
export function referenceToCatalogue(r: ReferenceTiling): CatalogueTiling {
	return {
		canonicalKey: r.id,
		k: r.k,
		family: r.family,
		source: r.source,
		renderCell: r.renderCell,
		certified: false,
		runIds: [],
		exactSource: r.exactSource,
		paramCell: r.paramCell,
		schlafli: r.schlafli,
		edge: r.edge,
		discoverer: r.discoverer,
		developed: r.developed,
		spherical: r.spherical,
		geometry: r.geometry,
		m: r.m,
		partition: r.partition,
		wallpaperGroup: r.wallpaperGroup,
		latticeShape: r.latticeShape,
		freedraw: r.freedraw,
	};
}

// Freedraw patterns ship as their own catalogue (public/freedraw/solutions.json — the same file the
// standalone /freedraw page reads), not as a pre-baked reference-atlas-*.json. There is nothing to bake:
// the pattern IS the record, and wrapping it in a build script would only duplicate 16 kB and give the two
// copies a way to drift. So the adaptation happens here, at load, the way the hyperbolic `edge` merge does.
//
// `renderCell` is a deliberate throwaway — every consumer that would draw it branches on `freedraw` first
// (thumbnails, /play canvas), exactly as they branch on `developed`/`spherical`.
const FREEDRAW_EMPTY_CELL = { cellPolygons: [], basis: [[1, 0], [0, 1]] } as unknown as TranslationalCellData;

function freedrawToReference(p: FreedrawPattern): ReferenceTiling {
	const stats = summarise(analyseFaces(p));
	freedrawStatsCache.set(p.id, stats);
	return {
		id: p.id,
		source: "freedraw",
		k: p.k,
		family: freedrawFamilyLabel(stats),
		renderCell: FREEDRAW_EMPTY_CELL,
		freedraw: p,
		geometry: "euclidean",
		discoverer: "Marek Čtrnáct",
		// Enumerated here and cross-checked against Čtrnáct's own solver (13 at k=1, 153 at k=2) — a matched
		// count, not a completeness proof of ours. That is exactly what "reproduced" means on this axis.
		certification: "reproduced",
	};
}

// Lazy client-side fetch of the static atlas. Cached across mode toggles so the ~4MB payload is pulled
// at most once per session (the browser also HTTP-caches it). Never bundled into the initial page.
let cache: ReferenceTiling[] | null = null;
let inflight: Promise<ReferenceTiling[]> | null = null;

export async function loadReferenceAtlas(): Promise<ReferenceTiling[]> {
	if (cache) return cache;
	if (inflight) return inflight;
	// The base atlas is required; the convex-irregular + isotoxal demo shelves are best-effort — a missing/broken
	// reference-atlas-{composable,isotoxal}.json degrades to an empty merge, never breaks the library.
	const bestEffort = (url: string) =>
		fetch(url)
			.then((res) => (res.ok ? (res.json() as Promise<ReferenceTiling[]>) : []))
			.catch(() => [] as ReferenceTiling[]);
	inflight = Promise.all([
		fetch("/reference-atlas.json").then((res) => {
			if (!res.ok) throw new Error(`reference-atlas.json: HTTP ${res.status}`);
			return res.json() as Promise<ReferenceTiling[]>;
		}),
		bestEffort("/reference-atlas-composable.json"),
		bestEffort("/reference-atlas-isotoxal.json"),
		bestEffort("/reference-atlas-mixed.json"),
		bestEffort("/reference-atlas-scaled.json"),
		bestEffort("/reference-atlas-polyomino.json"),
		bestEffort("/reference-atlas-islamic.json"),
		bestEffort("/reference-atlas-hyperbolic.json"),
		bestEffort("/reference-atlas-spherical.json"),
		// Freedraw is adapted from its own raw catalogue, not a reference-atlas-*.json (see freedrawToReference).
		fetch("/freedraw/solutions.json")
			.then((res) => (res.ok ? (res.json() as Promise<FreedrawPattern[]>) : []))
			.then((ps) => ps.map(freedrawToReference))
			.catch(() => [] as ReferenceTiling[]),
		fetch("/hyperbolic-developed.json")
			.then((res) => (res.ok ? (res.json() as Promise<Array<{ id: string; edge?: number }>>) : []))
			.catch(() => [] as Array<{ id: string; edge?: number }>),
	])
		.then(([base, composable, isotoxal, mixed, scaled, polyomino, islamic, hyperbolic, spherical, freedraw, devPatches]) => {
			// Merge the forced edge length ℓ onto each hyperbolic entry (keyed by developed.patch = patch id).
			// Best-effort: a missing patch just leaves `edge` undefined, and the card omits the ℓ readout.
			const edgeById = new Map<string, number | undefined>(
				(devPatches as Array<{ id: string; edge?: number }>).map((p) => [p.id, p.edge]),
			);
			for (const t of hyperbolic) {
				const e = edgeById.get(t.developed?.patch ?? t.id);
				if (typeof e === "number") t.edge = e;
			}
			const data = [...base, ...composable, ...isotoxal, ...mixed, ...scaled, ...polyomino, ...islamic, ...freedraw, ...hyperbolic, ...spherical];
			cache = data;
			inflight = null;
			return data;
		})
		.catch((err) => {
			inflight = null;
			throw err;
		});
	return inflight;
}

// Per-k lazy shards for the higher-k Čtrnáct atlas (k≥8, generated by scripts/build-reference-atlas.ts
// into public/reference-atlas-k{k}.json). Fetched only when that k is selected in the shelf filter, so
// a heavy shard (k=10 ≈ 73 MB) never loads unless the user opens it. Cached per-k across the session.
// NOTE: these records carry no m/partition/wallpaper classification yet (the build ctrnact path doesn't
// compute it) — they filter by k/tileClass/discoverer/certification and render, but are excluded from
// the M/partition/wallpaper facets until the build stamps those fields.
const shardCache = new Map<number, ReferenceTiling[]>();
const shardInflight = new Map<number, Promise<ReferenceTiling[]>>();

export async function loadReferenceAtlasShard(k: number): Promise<ReferenceTiling[]> {
	const cached = shardCache.get(k);
	if (cached) return cached;
	const existing = shardInflight.get(k);
	if (existing) return existing;
	const p = fetch(`/reference-atlas-k${k}.json`)
		.then((res) => {
			if (!res.ok) throw new Error(`reference-atlas-k${k}.json: HTTP ${res.status}`);
			return res.json() as Promise<ReferenceTiling[]>;
		})
		.then((data) => {
			shardCache.set(k, data);
			shardInflight.delete(k);
			return data;
		})
		.catch((err) => {
			shardInflight.delete(k);
			throw err;
		});
	shardInflight.set(k, p);
	return p;
}

// Per-k lazy shards for the convex-irregular demo shelf (k≥3, generated by scripts/build-composable-atlas.ts
