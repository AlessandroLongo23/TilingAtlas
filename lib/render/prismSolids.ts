// The k=1 spherical prism + antiprism families the Čtrnáct engine develops. Both are inscribable for
// every n (all vertices on a common sphere — two parallel n-gon circles at ±h/2), so they render as
// clean spherical tilings (an equatorial band of squares/triangles between two n-gon caps) exactly like
// the Platonic/Archimedean solids. Same generic Polyhedron shape, no renderer change. Coordinates are the
// developer's exact unit-sphere output (tools/ctrnact-oracle/develop_spherical.py, k=1); this palette
// {3,4,5,6,8,10} emits the n in {3,5,6,8,10} prisms and {4,5,6,8,10} antiprisms (n=4 prism = cube,
// n=3 antiprism = octahedron are Platonic and live there).

